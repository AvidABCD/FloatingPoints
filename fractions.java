class Main {
  public static void main(String[] args) {
    //basic fraction addition
    double fraction1 = 5/2;
    double fraction2 = 7/2;
    System.out.println(fraction1+fraction2); //prints: 5.0
    //subtraction
    System.out.println(fraction1-fraction2); //prints: 1.0
    //multiplication
    System.out.println(fraction1*fraction2); //prints: 6.0
    //division
    System.out.println(fraction1/fraction2); //prints: 0.6666666666666666
  }
}
