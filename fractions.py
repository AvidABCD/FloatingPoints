#basic fraction addition
fraction1 = 5/2
fraction2 = 7/2
print(fraction1+fraction2) #prints: 6.0

#fraction subtraction
print(fraction1-fraction2) #prints: -1.0

#fraction multiplication
print(fraction1*fraction2) #prints: 8.75

#fraction division
print(fraction1/fraction2) #prints: 0.7142857142857143
